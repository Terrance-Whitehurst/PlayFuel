def _fmt_iso_to_display(iso: str) -> str:
    """Parse ISO 8601 datetime string → 'Saturday at 9:00 AM' style.

    Handles both naive and timezone-aware strings. Falls back to raw string
    if parsing fails.
    """
    try:
        clean = iso.replace("Z", "+00:00")
        dt = datetime.fromisoformat(clean)
        day = dt.strftime("%A")
        h = dt.hour % 12 or 12
        ampm = "AM" if dt.hour < 12 else "PM"
        return f"{day} at {h}:{dt.minute:02d} {ampm}"
    except Exception:  # noqa: BLE001
        return iso


def _gap_status_text(gap_status: str) -> str:
    """Convert gap_status value to a parent-friendly phrase."""
    return {
        "ok": "comfortable break",
        "tight": "tight turnaround",
        "overrun": "potential schedule overrun",
        "no_next_match": "no next match today",
    }.get(gap_status, gap_status)


def _food_bucket_guidance(food_bucket: Optional[str]) -> str:
    """Convert food_bucket value to a parent-friendly guidance phrase."""
    if food_bucket is None:
        return "follow your normal post-match routine"
    return {
        "bag_only": "use bag food only—banana, pretzels, electrolyte drink",
        "portable": "grab portable pre-bought food quickly after the match ends",
        "quick_pickup": "pick up a quick fast-casual meal during the break",
        "light_meal": "there is time for a light meal—avoid heavy or greasy foods",
    }.get(food_bucket, "follow your normal post-match routine")


def _pickup_bucket_text(pickup_bucket: str) -> str:
    """Convert pickup_bucket value to a parent-friendly phrase."""
    return {
        "bring_portable": "have portable food ready before the match ends",
        "pickup_during_match": "consider picking up food during the final portion of the match if another trusted adult is present",
        "wait_until_end": "wait until the match ends before getting food",
    }.get(pickup_bucket, "follow your normal routine")


class TemplateProvider:
    """Deterministic template-based explanation provider.

    Used by default (no API keys required) and in all tests. Generates
    parent-friendly prose via f-strings — never invents content. Every field
    in the output comes directly from PlanExplanationInput.

    Returns PlanExplanation with provider='template', model=None.
    """

    def explain_plan(
        self,
        inp: "PlanExplanationInput",  # noqa: F821
    ) -> "PlanExplanation":  # noqa: F821
        from playfuel_api.models.api import PlanExplanation

        return PlanExplanation(
            summary=self._build_summary(inp),
            scenario_explanations=self._build_scenario_explanations(inp),
            weather_note=self._build_weather_note(inp),
            food_note=self._build_food_note(inp),
            safety_note=self._build_safety_note(inp),
            provider="template",
            model=None,
            generated_at=datetime.now(tz=timezone.utc),
        )

    # MARK: - Internal builders (all pure functions of inp — no invented content)

    @staticmethod
    def _build_summary(inp: "PlanExplanationInput") -> str:  # noqa: F821
        venue = inp.venue_name or "the venue"
        round_part = f"a {inp.match_round_label} match" if inp.match_round_label else "a match"
        time_part = _fmt_iso_to_display(inp.match_start_iso)
        heat_note = " Extreme heat conditions are expected today." if inp.extreme_heat_risk else ""
        is_doubles = getattr(inp, "match_type", "singles") == "doubles"

        # Derive normal-scenario duration from scenarios (avoids hardcoding 120 min).
        normal_s = next((s for s in inp.scenarios if s.scenario == "normal"), None)
        normal_min = normal_s.duration_min if normal_s else (90 if is_doubles else 120)

        if is_doubles:
            subject = "Your child's doubles team"
            match_desc = f"{round_part}"
        else:
            subject = "Your player's tournament day"
            match_desc = f"includes {round_part}"

        summary = (
            f"{subject} at {venue} {match_desc} "
            f"scheduled to start at {time_part}. "
            f"We've prepared three scenarios—short, normal, and long—to cover "
            f"different match durations. "
            f"The normal scenario (~{friendly_duration(normal_min)}) is used as the primary planning reference."
            f"{heat_note}"
        )

        # Conservative opponent-notes acknowledgment (PLAYER_SCOUTING_V1.md §D.4).
        # Count-only: never quotes note body, never reveals source.
        # Real tactical paraphrase waits for actual LLM provider — OQ-SCOUT-LLM-1.
        notes = getattr(inp, "opponent_notes", [])
        if notes:
            n = len(notes)
            word = "observation" if n == 1 else "observations"
            summary += (
                f" Your notes mention {n} prior {word}"
                f" — review the player profile for tactics."
            )

        return summary

    @staticmethod
    def _build_scenario_explanations(
        inp: "PlanExplanationInput",  # noqa: F821
    ) -> dict[str, str]:
        is_doubles = getattr(inp, "match_type", "singles") == "doubles"
        subject = "your doubles team" if is_doubles else "you"
        result: dict[str, str] = {}
        for s in inp.scenarios:
            gap = _gap_status_text(s.gap_status)
            food = _food_bucket_guidance(s.food_bucket)
            pickup = _pickup_bucket_text(s.pickup_bucket)
            result[s.scenario] = (
                f"In the {s.scenario} scenario ({friendly_duration(s.duration_min)} match): "
                f"{subject}'ll have a {gap}. "
                f"For food: {food}. "
                f"For parent pickup: {pickup}."
            )
        return result

    @staticmethod
    def _build_weather_note(
        inp: "PlanExplanationInput",  # noqa: F821
    ) -> Optional[str]:
        if inp.weather_temp_f is None:
            return None

        humidity_str = ""
        if inp.weather_humidity_pct is not None:
            humidity_str = f" with {inp.weather_humidity_pct}% humidity"

        flag_note = ""
        if "very_hot" in inp.weather_flags:
            flag_note = " Keep the player in shade and ensure extra fluids are available between points."
        elif "hot" in inp.weather_flags:
            flag_note = " Keep the player cool with shade and extra fluids during breaks."
        elif "cold" in inp.weather_flags:
            flag_note = " Ensure the player stays warm between matches."

        rain_note = ""
        if "rain_risk" in inp.weather_flags:
            rain_note = " There is a chance of rain—bring a rain plan and extra dry clothes."

        return (
            f"Today's conditions are {inp.weather_temp_f:.0f}°F{humidity_str}."
            f"{flag_note}{rain_note}"
        ).strip()

    @staticmethod
    def _build_food_note(
        inp: "PlanExplanationInput",  # noqa: F821
    ) -> Optional[str]:
        if inp.bag_fallback_only:
            return "Pack from home \u2014 bag-only window between matches."
        if not inp.food_recommendations:
            return None
        top3 = inp.food_recommendations[:3]
        parts: list[str] = []
        for r in top3:
            drive = f", {r.drive_time_minutes} min drive" if r.drive_time_minutes is not None else ""
            parts.append(f"{r.name} ({r.category}{drive})")
        return f"Nearby options: {', '.join(parts)}."

    @staticmethod
    def _build_safety_note(
        inp: "PlanExplanationInput",  # noqa: F821
    ) -> str:
        if inp.extreme_heat_risk and inp.heat_emergency_text:
            return f"{inp.heat_emergency_text}\n\n{inp.user_disclaimer}"
        return inp.user_disclaimer


