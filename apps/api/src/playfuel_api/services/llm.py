# ── Factory ───────────────────────────────────────────────────────────────────


def get_llm_provider() -> LLMProvider:
    """Return the configured LLM provider.

    Provider selection:
        "auto"      → Anthropic if ANTHROPIC_API_KEY non-empty, else
                      OpenAI if OPENAI_API_KEY non-empty, else TemplateProvider.
        "template"  → TemplateProvider always.
        "anthropic" → AnthropicProvider (raises NotImplementedError if SDK missing).
        "openai"    → OpenAIProvider    (raises NotImplementedError if SDK missing).

    Never raises in 'auto' mode — always falls back to TemplateProvider.
    """
    from playfuel_api.settings import get_settings

    settings = get_settings()
    provider_name = settings.llm_provider.lower()

    if provider_name == "template":
        return TemplateProvider()

    if provider_name == "anthropic":
        return AnthropicProvider(
            api_key=settings.anthropic_api_key,
            model=settings.anthropic_model,
        )

    if provider_name == "openai":
        return OpenAIProvider(
            api_key=settings.openai_api_key,
            model=settings.openai_model,
        )

    # "auto" or unrecognised → first available key, else TemplateProvider
    if settings.anthropic_api_key:
        try:
            return AnthropicProvider(
                api_key=settings.anthropic_api_key,
                model=settings.anthropic_model,
            )
        except NotImplementedError:
            _logger.warning(
                "AnthropicProvider unavailable (SDK not installed). "
                "Falling back to TemplateProvider."
            )

    if settings.openai_api_key:
        try:
            return OpenAIProvider(
                api_key=settings.openai_api_key,
                model=settings.openai_model,
            )
        except NotImplementedError:
            _logger.warning(
                "OpenAIProvider unavailable (SDK not installed). "
                "Falling back to TemplateProvider."
            )

    return TemplateProvider()


# ── build_explanation_input ────────────────────────────────────────────────────


def build_explanation_input(
    plan: "Plan",  # noqa: F821
    match: "MatchRow",  # noqa: F821
    next_match: "Optional[MatchRow]",  # noqa: F821
    snapshot: "Optional[WeatherSnapshotRow]",  # noqa: F821
    food_options_list: "list[FoodOption]",  # noqa: F821
    venue_name: str = "",
) -> "PlanExplanationInput":  # noqa: F821
    """Build a PlanExplanationInput from the assembled plan and raw components.

    Every field is derived from structured inputs — nothing is invented.
    Called in routes/plans.py after build_plan_envelope() returns.
    """
    from playfuel_api.models.api import (
        FoodRecommendationSummary,
        PlanExplanationInput,
        ScenarioSummary,
    )
    from playfuel_api.rules.hard_coded_strings import HEAT_EMERGENCY_TEXT, USER_DISCLAIMER

    # Scenarios
    scenarios = [
        ScenarioSummary(
            scenario=s.scenario.value,
            duration_min=s.duration_min,
            gap_status=s.gap_status.value,
            food_bucket=s.food_strategy.bucket.value if s.food_strategy else None,
            pickup_bucket=(
                s.pickup_strategy.bucket.value
                if s.pickup_strategy and s.pickup_strategy.bucket
                else "wait_until_end"
            ),
        )
        for s in plan.scenario_plans
    ]

    # Food recommendations (name + category + drive_time; no address / place_id)
    food_recs = [
        FoodRecommendationSummary(
            name=f.name,
            category=f.category,
            drive_time_minutes=f.drive_time_minutes,
            is_draft=f.is_draft if hasattr(f, "is_draft") else False,
        )
        for f in (food_options_list or [])
    ]

    # Weather flags
    weather_flags: list[str] = []
    weather_temp_f: Optional[float] = None
    weather_humidity_pct: Optional[int] = None
    extreme_heat_risk = False
    if snapshot is not None:
        weather_temp_f = snapshot.temp_f
        weather_humidity_pct = int(snapshot.humidity_pct)
        if snapshot.flag_hot:
            weather_flags.append("hot")
        if snapshot.flag_very_hot:
            weather_flags.append("very_hot")
        if snapshot.flag_humid:
            weather_flags.append("humid")
        if snapshot.flag_cold:
            weather_flags.append("cold")
        if snapshot.flag_windy:
            weather_flags.append("windy")
        if snapshot.flag_rain_risk:
            weather_flags.append("rain_risk")
        extreme_heat_risk = snapshot.flag_extreme_heat_risk

    # Derive match_type from match.format (doubles-spec §C.2).
    match_type_val = (match.format or "singles").lower()
    if match_type_val not in ("singles", "doubles"):
        match_type_val = "singles"

    return PlanExplanationInput(
        venue_name=venue_name,
        match_start_iso=match.scheduled_start.isoformat(),
        match_round_label=match.round_label,
        next_match_estimated_iso=(
            next_match.scheduled_start.isoformat() if next_match else None
        ),
        weather_temp_f=weather_temp_f,
        weather_humidity_pct=weather_humidity_pct,
        weather_flags=weather_flags,
        extreme_heat_risk=extreme_heat_risk,
        scenarios=scenarios,
        food_recommendations=food_recs,
        bag_fallback_only=plan.bag_fallback_only,
        heat_emergency_text=HEAT_EMERGENCY_TEXT if extreme_heat_risk else None,
        user_disclaimer=USER_DISCLAIMER,
        match_type=match_type_val,
    )
